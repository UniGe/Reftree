function getObjectText(code) {//default is italian 
    var arrayofcommonlanguagescultures = [];
    if (culture.substring(0, 2) === 'en')//Inglese
    {
        arrayofcommonlanguagescultures.push('en-GB');
        arrayofcommonlanguagescultures.push('en-US');
        arrayofcommonlanguagescultures.push('en-CA');
    }
    else if (culture.substring(0, 2) === 'it') { //Italiano
        arrayofcommonlanguagescultures.push('it-IT');
        arrayofcommonlanguagescultures.push('it-CH');
    }
    else arrayofcommonlanguagescultures.push(culture);

    for (i = 0; i < arrayofcommonlanguagescultures.length; i++) {
        var culturetofind = arrayofcommonlanguagescultures[i];
        if (h[culturetofind + '@' + code] !== undefined)
            return h[culturetofind + '@' + code];

    }
    //italian is default
    if (h['it-IT@' + code] !== undefined)
        return h['it-IT@' + code];
    //if no entry has been found i return the code itself
    return code;

}